    <h2 style="text-align:center;">Cara Pemesanan Rumah</h2>
    <hr>

    <ol>
        <li>
            <p> Daftar <br>
                Lakukan Daftar dengan memasukan data diri, untuk bisa melakukan pemesanan rumah.
            </p>
        </li>
        <li>
            <p>
                Login<br>
                Setelah daftar segera untuk login
            </p>
        </li>
        <li>
            <p>
                Cari Rumah<br>
                Temukan rumah yang sesuai dengan keinginan anda
            </p>
        </li>
        <li>
            <p>
                Pesan<br>
                Setelah menemukan rumah yang sesuai, KLik "Pesan" untuk menambahkan ke daftar booking.
            </p>
        </li>
        <li>
            <p>
                Tentukan Tanggal Pemesanan<br>
                Untuk melanjutkan pemesanan rumah, Klik "Booking" untuk menambahkan waktu pemesanan dan menyelesaikan booking.
            </p>
        </li>
        <li>
            <p>
                Transfer<br>
                Setelah itu akan masuk ke halaman transfer, calon penyewa rumah dapat melakukan transfer DP 10% dari harga rumah dan memasukan foto bukti tranfer.
            </p>
        </li>
        <li>
            <p>
                Hubungi Pemilik<br>
                Calon Penyewa harap menunggu konfirmasi dari pemilik rumah 1-3 hari.
            </p>
        </li>
        <li>
            <p>
                Konfirmasi<br>
                Terakhir silakan komunikasikan dengan pemilik rumah untuk mendapatkan informasi lebih lanjut.
            </p>
        </li>
    </ol>
